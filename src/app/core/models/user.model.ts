/**
 * Model for a user of the application.
 */
export class User {

    /**
     * The user's UID.
     */
    uid: string;

    /**
     * The user's full name
     */
    fullName: string;

    /**
     * The user's email.
     */
    email: string;

    /**
     * The URL location of the user's profile photo.
     */
    photoUrl: string;

    /**
     * The URL location of the user's resume.
     */
    resumeUrl: string;

}
