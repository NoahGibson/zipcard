/**
 * Model for a user of the application.
 */
export class User {

    /**
     * The user's UID.
     */
    uid: string;

    /**
     * The user's first name.
     */
    firstName: string;

    /**
     * The user's last name.
     */
    lastName: string;

    /**
     * The URL location of the user's profile photo.
     */
    photoUrl: string;

    /**
     * The URL location of the user's resume.
     */
    resumeUrl: string;

}
